  let answer;

  // 페이지의 숫자를 더하는 함수
  function plusValue(arr) {
    let plusResult = 0;
    let result = 0;
    let a = String(arr);

    for (let i = 0; i < a.length; i++) {
      plusResult += parseInt(a[i]);
    };

    return result
  };

